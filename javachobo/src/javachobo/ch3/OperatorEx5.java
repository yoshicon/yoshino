package javachobo.ch3;

public class OperatorEx5 {

	public static void main(String[] args) {
		int q = 10, w = 4;

		System.out.printf("%d + %d = %d \n", q, w, q + w);
		System.out.printf("%d - %d = %d \n", q, w, q - w);
		System.out.printf("%d 8 %d = %d \n", q, w, q * w);
		System.out.printf("%d / %d = %d \n", q, w, q / w);
		System.out.printf("%d + %f = %f \n", q, (float)w, q / (float)w);

	}

}
